/** Deduplicated pool: the categorized sample texts + extra prompts. */
export const SUGGESTIONS: string[] = Array.from(
  new Set([...CAT_ORDER.flatMap((k) => IDEA_POOL[k]), ...EXTRA]),
);

/** N random suggestions, excluding any already shown. */
export function randomSuggestions(n: number, exclude: string[] = []): string[] {
  const pool = SUGGESTIONS.filter((s) => !exclude.includes(s));
  const out: string[] = [];
  const used = new Set<number>();
  const target = Math.min(n, pool.length);
  while (out.length < target) {
    const i = Math.floor(Math.random() * pool.length);
    if (used.has(i)) continue;
    used.add(i);
    out.push(pool[i]);
  }
  return out;
}
